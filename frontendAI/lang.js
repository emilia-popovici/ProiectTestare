// --- FUNCȚIA DE TRADUCERE ---
function updateLanguage(lang) {
  localStorage.setItem('preferredLang', lang);

  // Traducem elementele din HTML care au atributul data-lang
  document.querySelectorAll('[data-lang]').forEach(el => {
    const key = el.getAttribute('data-lang');
    if (translations[lang] && translations[lang][key]) {
      const text = translations[lang][key];

      // Dacă e input, schimbăm placeholder-ul
      if (el.tagName === 'INPUT') {
        el.placeholder = text;
      } else {
        // Altfel schimbăm conținutul (innerHTML permite si <strong>...</strong>)
        el.innerHTML = text;
      }
    }
  });
}

// Helper global pentru a lua un text în JS
function getTrans(key) {
  const lang = localStorage.getItem('preferredLang') || 'ro';
  if (translations[lang] && translations[lang][key]) {
    return translations[lang][key];
  }
  return key;
}